drop table if exists dwh.people_dim;

CREATE TABLE dwh.people_dim
(
	 people_key  int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	 sales_name   varchar NOT NULL
);

insert into dwh.people_dim (sales_name)
select distinct
	p.person 
from stg.people p;

select 
	*
from dwh.people_dim;

